package laz.llunaplenafnsb.items;

/**
 * ImageItem.
 */
public class ImageItem {

    private int mWidth;
    private int mHeight;
    private String mSrc;


    public int getWidth() {
        return mWidth;
    }

    public void setWidth(int width) {
        mWidth = width;
    }

    public int getHeight() {
        return mHeight;
    }

    public void setHeight(int height) {
        mHeight = height;
    }

    public String getSrc() {
        return mSrc;
    }

    public void setSrc(String src) {
        mSrc = src;
    }
}
